-- Options
------------

-- Set the leader key
vim.g.mapleader = "<Space>"

-- Tabs

-- Set amount to spaces tab creates
vim.opt.softtabstop = 4
-- Set amount of spaces used for indentation (e.g. >> or <<)
vim.opt.shiftwidth = 4
-- Use spaces instead of tabs
vim.opt.expandtab = true

vim.opt.wrap = false
